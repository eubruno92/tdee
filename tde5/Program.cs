// para declarar uma variável do tipo double como um tipo nullable (que pode conter null), você deve adicionar um ? após o tipo da variável.

double? nota = 7.80;

Console.WriteLine(nota);

// Neste exemplo, double? indica que nota é uma variável do tipo double que pode conter um valor null. O valor 7.80 é atribuído a nota, e então esse valor é exibido usando Console.WriteLine.

