// O que é Camel Case ? Dê um exemplo de sua aplicação.

// Ela é uma convenção de nomenclatura usada por developers para deixar o seu código mais legível para outros DEVs. 

// Através dela você deixa a primeira letra de cada palavra (de um termo composto) em maiúsculo, com exceção do primeiro termo. Por exemplo:

// De tenteleressafrase para tenteLerEssaFrase. 
// Qual fica mais fácil de ler? Certamente a segunda forma, não é mesmo? 

// Pode parecer um simples detalhe, mas isso facilita muito a programação, principalmente quando outra pessoa desenvolvedora lê seu código em um projeto em comum. Afinal, as palavras são escritas juntas, pois não se pode deixar espaços na hora de codar. 

// Camel Case é uma convenção de nomenclatura em que a primeira letra de cada palavra é maiúscula, exceto a primeira palavra. É comumente usado em programação e escrita de códigos para nomear variáveis, funções, métodos, classes e outras entidades de forma a torná-las legíveis, enquanto ainda evita espaços, que muitas vezes não são permitidos em nomes de variáveis ou funções em muitas linguagens de programação.

//Lower Camel Case (camelCase): onde a primeira letra do nome é minúscula. Por exemplo, minhaVariavel, calcularDistancia, obterValorTotal.
